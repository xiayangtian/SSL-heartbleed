def recvmsg(s):
    hdr = recvall(s, 5)
    if hdr is None:
        print('读取数据失败')
        return None, None, None
    #接收数据，解开前三个包分别为类型，版本，长度
    typ, ver, ln = struct.unpack('>BHH', hdr)
    #按长度接收数据
    pay = recvall(s, ln, 10)
    if pay is None:
        print('读取数据失败')
        return None, None, None
    print(' ... 返回消息: 类型 = %d, 版本 = %04x, 长度 = %d' % (typ, ver, len(pay)))
    return typ, ver, pay


